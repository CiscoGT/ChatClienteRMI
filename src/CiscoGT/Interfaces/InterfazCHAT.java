    private void userLogin() 
    {
        username = userNameInputText.getText();
        try 
        {
            users = chat.getAllUsers();
        } 
        catch (RemoteException ex) 
        {
            Logger.getLogger(InterfazCHAT.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (users.contains(username)) 
        {
            JOptionPane.showMessageDialog(null, "Este usuario ya existe");
            System.exit(0);
        } 
        else 
        {
            try 
            {
                new VerChat(chat, username);   // pasa la referencia "chat" y el nombre del usuario registrado
            }                                   // y crea un nuevo ChatView
            catch (RemoteException ex) 
            {      
                Logger.getLogger(InterfazCHAT.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.setVisible(false);
        }
    }
